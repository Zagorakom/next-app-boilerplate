const fn = arr => {
	console.time('fn1 reduce');
	const result = Object.keys(
		arr.reduce((acc, curr) => {
			acc[curr] = true;
			return acc;
		}, {}),
	);
	console.timeEnd('fn1 reduce');
	return result;
};

const fn2 = arr => {
	console.time('fn2 push');
	let uniqueArray = [];
	let obj = {};

	for (let i = 0; i < arr.length; i++) {
		if (!obj[arr[i]]) {
			uniqueArray.push(arr[i]);
			obj[arr[i]] = true;
		}
	}
	console.timeEnd('fn2 push');
	return uniqueArray;
};

const fn3 = arr => {
	console.time('fn3 Set');
	const result = [...new Set(arr)];
	console.timeEnd('fn3 Set');
	return result;
};

const fn4 = arr => {
	console.time('fn4 includes');
	const result = [];
	for (const item of arr) {
		if (!result.includes(item)) {
			result.push(item);
		}
	}
	console.timeEnd('fn4 includes');
	return result;
};

